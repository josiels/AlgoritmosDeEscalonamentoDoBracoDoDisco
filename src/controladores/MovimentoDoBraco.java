package controladores;

public class MovimentoDoBraco {

    private int posicaoAtual;
    private int proximaPosicao;
    private int totalTrajeto;

    public int getPosicaoAtual() {
        return posicaoAtual;
    }

    public void setPosicaoAtual(int posicaoAtual) {
        this.posicaoAtual = posicaoAtual;
    }

    public int getProximaPosicao() {
        return proximaPosicao;
    }

    public void setProximaPosicao(int proximaPosicao) {
        this.proximaPosicao = proximaPosicao;
    }

    public int getTotalTrajeto() {
        return totalTrajeto;
    }

    public void setTotalTrajeto(int totalTrajeto) {
        this.totalTrajeto = totalTrajeto;
    }
    
}
